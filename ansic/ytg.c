float year[12][5]={
    {1.2 , 1.6 , 1.5 , 0.7 , 0.5},
    {0.9 , 0.8 , 0.7 , 0.7 , 0.7},
    {0.6 , 0.8 , 0.9 , 0.9 , 1.2},
    {0.7 , 1.9 , 1.2 , 1.2 , 0.8},
    {1.2 , 1.2 , 1.0 , 0.8 , 0.8},
    {0.5 , 0.6 , 0.7 , 0.7 , 0.6},
    {0.9 , 0.8 , 1.5 , 1.3 , 1.9},
    {0.8 , 0.7 , 0.6 , 0.5 , 0.6},
    {0.7 , 0.5 , 0.5 , 1.4 , 0.8},
    {0.8 , 1.5 , 1.4 , 0.5 , 1.6},
    {1.5 , 0.6 , 1.4 , 0.9 , 1.0},
    {0.9 , 1.6 , 0.9 , 1.7 , 0.7}
};

float month[12]={0.6 , 0.7 , 1.8 , 0.9 , 0.5 , 1.6 , 0.9 , 1.5 , 1.8 , 0.8 , 0.9 , 0.5};

float day[30]={0.5 , 1.0 , 0.8 , 1.5 , 1.6 , 1.5 , 0.8 , 1.6 , 0.8 , 1.6 , 0.9 , 1.7 ,
    0.8 , 1.7 , 1.0 , 0.8 , 0.9 , 1.8 , 0.5 , 1.5 , 1.0 , 0.9 , 0.8 , 0.9 , 1.5 , 1.8 ,
    0.7 , 0.8 , 1.6 , 0.6};

float hour[12]={1.6 , 0.6 , 0.7 , 1.0 , 0.9 , 1.6 , 1.0 , 0.8 , 0.8 , 0.9 , 0.6 , 0.6};

int count[80]={0,};


int main()
{
    int i,j,k,m,h;
    int w=0;
    float buf = 0;

    float all = 60*12*30*12;

    for(i=0;i<12;i++)
        for(j=0;j<5;j++)
        {
            for(k=0;k<12;k++)
                for(m=0;m<30;m++)
                {
             
                    for(h=0;h<12;h++)
                    {
                        buf = year[i][j] + month[k] + day[m] + hour[h];
                        buf *= 10;
                        w = (int)buf;
                        count[w]++;
                    }
                }
        }


    float upsum = 0;
    for(i=0;i<80;i++)
    {
        for(j=0;j<i;j++)
        {
            upsum += (float)count[j]/all;
        }
        if(count[i]==0)continue;
        printf("%0.1f -> %6d %%: %0.7f ups: %0.7f\n", ((float)i)/10, count[i], 
                (float)count[i]/all, upsum);
        upsum = 0;
    }

    return 0;
}
